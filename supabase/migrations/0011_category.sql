-- 카테고리 계층 테이블 (대분류/중분류/세부항목)
create table category (
  id         uuid primary key default gen_random_uuid(),
  parent_id  uuid references category(id) on delete cascade,
  depth      smallint not null default 0,
  label      text not null,
  slug       text unique not null,
  sort_order smallint not null default 0,
  created_at timestamptz not null default now()
);

create index idx_category_parent on category(parent_id);
create index idx_category_depth  on category(depth);

-- 파트너-카테고리 다대다 연결 (기존 field TEXT 대체)
create table partner_category (
  partner_id  uuid not null references partner(id) on delete cascade,
  category_id uuid not null references category(id) on delete cascade,
  primary key (partner_id, category_id)
);

create index idx_pc_partner  on partner_category(partner_id);
create index idx_pc_category on partner_category(category_id);

-- request 테이블에 category_id 추가 (req_type은 하위 호환 유지)
alter table request add column category_id uuid references category(id);

-- RLS
alter table category enable row level security;
create policy category_select_all on category for select using (true);

alter table partner_category enable row level security;
create policy pc_select_own on partner_category for select
  using (partner_id in (select id from partner where auth_user_id = auth.uid()));
create policy pc_insert_own on partner_category for insert
  with check (partner_id in (select id from partner where auth_user_id = auth.uid()));
create policy pc_delete_own on partner_category for delete
  using (partner_id in (select id from partner where auth_user_id = auth.uid()));

